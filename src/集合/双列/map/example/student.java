package 集合.双列.map.example;

public class student {
    private String name;
    private int score;

    public student(){}
    public student(String na,int score){
        this.name=na;
        this.score=score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
